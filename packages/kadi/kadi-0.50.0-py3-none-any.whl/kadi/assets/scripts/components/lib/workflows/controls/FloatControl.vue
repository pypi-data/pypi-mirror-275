<template>
  <input v-model.number="value" class="form-control d-inline" :title="value" @pointerdown.stop>
</template>

<script>
import workflowControlMixn from 'scripts/components/mixins/workflow-control-mixin';

export default {
  mixins: [workflowControlMixn],
  props: {
    defaultValue: Number,
  },
  methods: {
    convertValue() {
      const value = Number(this.value);

      if (window.isNaN(value)) {
        this.value = this.defaultValue;
      } else {
        this.value = value;
      }
    },
  },
};
</script>
